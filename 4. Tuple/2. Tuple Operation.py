# Tuple with different values

myTuple = ((1, 2, 3), ('a', 'b', 'c'), ['a', 1, 2, 3], {"stage": "production"})
print(myTuple)
print(myTuple[0])
print(myTuple[1])
print(myTuple[2])
print(myTuple[3])
print(myTuple[3]["stage"])