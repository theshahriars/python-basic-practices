# If-elif-else statement

# Example 1

num = 16

if num == 6:
    print('The number is 6')
elif num == 7:
    print('The number is 7')
elif num == 8:
    print('The number is 8')
elif num == 9:
    print('The number is 9')
else:
    print('The number is not 6, 7, 8, 9')

