# Boolean Expression
# There are two types of boolean value 'true' & 'false'

print(2 == 2) # true

print(2 == 3) # false

print(2 != 4) # true

print(3 > 2) # true

print(2 > 2) # false

print(2 >= 2) # true

print(1 < 2) # true

print(2 < 2) # false

print(2 <= 2) # true

print((2 + 3) > 2) # true

print('Hello' == 'hello') # false

print('Hello' != 'hello') # true

print('hello' == 'hello') # true