# Taking input for first number
print("Enter first number:")
firstNumber = input()

# Taking input for second number
print("Enter second number:")
secondNumber = input()

# Summation
print("Summation of", firstNumber, "+", secondNumber, "is:")
summation = int(firstNumber) + int(secondNumber)
print(summation)

# Subtraction
print("Subtraction of", firstNumber, "-", secondNumber, "is:")
subtraction = int(firstNumber) - int(secondNumber)
print(subtraction)

# Multiplication
print("Multiplication of", firstNumber, "*", secondNumber, "is:")
multiplication = int(firstNumber) * int(secondNumber)
print(multiplication)

# Division
print("Division of", firstNumber, "/", secondNumber, "is:")
division = int(firstNumber) / int(secondNumber)
print(division)

# Quotient
print("Quotient of", firstNumber, "/", secondNumber, "is:")
quotient = int(firstNumber) // int(secondNumber)
print(quotient)

# Reminder
print("Reminder of", firstNumber, "/", secondNumber, "is:")
reminder = int(firstNumber) // int(secondNumber)
print(reminder)

# Exponentiation
print("Exponentiation of", firstNumber, "^", secondNumber, "is:")
exponentiation = int(firstNumber) ** int(secondNumber)
print(exponentiation)
