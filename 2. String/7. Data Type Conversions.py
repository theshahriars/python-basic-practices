# Data type conversion

# Example one
string = "123"
integerData = int(string)
print(integerData)

# Example two
string = "123.456"
floatData = float(string)
print(floatData)

# Example three
integerData = 123
string = str(integerData)
print(string)

# Example four
floatData = 123.234
string = str(floatData)
print(string)