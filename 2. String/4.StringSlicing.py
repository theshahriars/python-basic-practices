# String slicing
string = "Shahriar Shabbir"

# Printing first character
print(string[0])

# Printing last character
print(string[-1])

# Printing full string
print(string[:])

# Printing full string
print(string[0:])

# Slicing string from 1st index to 6th index
print(string[0:5])

# Slicing string from 4th index to 11th index
print(string[3:10])

# Slicing string from 1st index and the 11th index will count from right to left (reverse count)
print(string[0:-11])