# String justification

# Example one
string = "Hello"
justifiedString = string.ljust(10, ' ')
print(justifiedString)

# Example two
string = "Hello"
justifiedString = string.ljust(10, '*')
print(justifiedString)

# Example three
string = "Hello"
justifiedString = string.rjust(10, ' ')
print(justifiedString)

# Example four
string = "Hello"
justifiedString = string.rjust(10, '*')
print(justifiedString)

# Example five
string = "Hello"
justifiedString = string.center(11, ' ')
print(justifiedString)

# Example six
string = "Hello"
justifiedString = string.center(11, '*')
print(justifiedString)