# Uppercase conversion
string = "This is string one"
upperCase = string.upper()
print(upperCase)

# Lowercase conversion
string = "This is string two"
lowerCase = string.lower()
print(lowerCase)

# Capitalize of string
string = "this is string three"
capitalizedCase = string.capitalize()
print(capitalizedCase)

