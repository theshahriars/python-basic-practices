# String inside single quote
string = 'This is a string wrapped by single quote.'
print(string)

# String inside double quote
string = "This is a string wrapped by double quote."
print(string)

# String inside triple single quote and multi line string
string = '''This is a string wrapped by triple quote
and this is also a multi line string.'''
print(string)

# String inside triple double quote and multi line string
string = """This is a string wrapped by triple quote
and this is also a multi line string."""
print(string)

# Single quote inside double quote
string = "This is Shahriar\'s github account."
print(string)

# Double quote inside single quote
string = 'I like "PYTHON".'
print(string)

# Printing new line
string = 'Hello!\nHow are you?'
print(string)

# Printing tab
string = '\tHow are you?'
print(string)

# Printing back slash
string = 'D:\\PYTHON\\CODE'
print(string)

# Raw string
string = r"\\This is a raw text\n."
print(string)

# Unicode string
string = u"This is an unicode text."
print(string)

# Printing id of a string
string = "Hello World."
id = id(string)
print(id)
