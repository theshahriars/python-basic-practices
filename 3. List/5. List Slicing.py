# List slicing
myList = [1, 2, 7, 9, 5, 6]

# Printing first item
print(myList[0])

# Printing last item
print(myList[-1])

# Printing all items
print(myList[0:])

# Printing all items without first two item
print(myList[1:])

# Printing first 5 items
print(myList[0:4])

# Printing all items without last two item
print(myList[0:-2])
